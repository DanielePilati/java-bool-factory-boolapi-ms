package com.boolfactory.BoolAPI.controller.objects;

public class Work {
	                       
		private String name ;
	    private double rate;
	          
	    // Getter
	    public String getName() {
	      return name;
	    }
	    public double getRate() {
		      return rate;
		    }

	    // Setter
	    public void setName(String newName) {
	      this.name = newName;
	    }
	    public void setRate(double newRate) {
		      this.rate = newRate;
		    }
}
